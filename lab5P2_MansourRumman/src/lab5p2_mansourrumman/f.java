    private void jtpMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtpMouseClicked
        if (evt.isMetaDown()) {
            int row = jtp.getClosestRowForLocation(
                    evt.getX(), evt.getY());
            jtp.setSelectionRow(row);
            Object v1 = jtp.getSelectionPath().getLastPathComponent();
            node = (DefaultMutableTreeNode) v1;
            if (node.getUserObject() instanceof gente) {
                c = (gente) node.getUserObject();
                jpop.show(evt.getComponent(), evt.getX(), evt.getY());
            }
        }
    }//GEN-LAST:event_jtpMouseClicked

